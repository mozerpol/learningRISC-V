########################################################
##                                                    ##
##                     JAL, JALR                      ##
##                                                    ##
########################################################
########################################
##   Prepare registers, reset them    ##
########################################
addi  x1,  x0,   0     # x1 = 0x00000000
addi  x12, x0,   0     # x12 = 0x00000000
addi  x13, x0,   0     # x13 = 0x00000000
addi  x14, x0,   0     # x14 = 0x00000000
addi  x16, x0,   0     # x16 = 0x00000000
addi  x17, x0,   0     # x17 = 0x00000000
addi  x18, x0,   0     # x18 = 0x00000000
addi  x19, x0,   0     # x19 = 0x00000000
addi  x20, x0,   0     # x20 = 0x00000000
addi  x21, x0,   0     # x21 = 0x00000000
addi  x22, x0,   0     # x22 = 0x00000000
addi  x23, x0,   0     # x23 = 0x00000000
addi  x24, x0,   0     # x24 = 0x00000000
addi  x25, x0,   0     # x25 = 0x00000000
##############
##   JAL    ##
##############
auipc x12, 0           # ... 1.
jal   x13, loop22      # ... 2.
loop23:
addi  x1,  x1,   1     # x1 = 0x00000002 6.
addi  x1,  x1,   1     # x1 = 0x00000003 7.
jal   x16, loop24      # ... 8.
loop22:
addi  x1,  x1,   1     # x1 = 0x00000001 3.
sub   x14, x13,  x12   # x14 = 0x00000008 4.
jal   x14, loop23      # ... 5.
loop24:
addi  x1,  x1,   1     # x1 = 0x00000004 9.
sub   x17, x16,  x14   # x17 = 0xfffffff4 10.
addi  x1,  x0,   0     # x1 = 0x00000000 11.
##############
##   JALR   ##
##############
auipc x18, 0           # ... 1.
jalr  x19, x18,  8     # ... 2.
sub   x20, x19,  x18   # x20 = 0x00000008 3.
jalr  x20, x18,  32    # ... 4.
addi  x1,  x1,   1     # x1 = 0x00000001 9.
auipc x23, 0           # ... 10.
sub   x24, x22,  x21   # x24 = 0x00000008 11.
jalr  x24, x23,  28    # ... 12.
auipc x21, 0           # ... 5.
sub   x21, x21,  x20   # x21 = 0x00000010 6.
auipc x21,  0          # ... 7.
jalr  x22, x21,  -24   # ... 8.
addi  x1,  x1,   1     # x1 = 0x00000002 13.
auipc x25, 0           # ... 14.
sub   x25, x25,  x24   # x25 = 0x00000014 15.
